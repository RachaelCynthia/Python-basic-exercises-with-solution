# program to interchange first and last element in a list
list1 = [8, 37, 10, 5, 40, 6]
list1[0], list1[-1] = list1[-1], list1[0]
print(list1)


# program to swap element in the list
list2 = ["Jim", "Pam"]
list2[0], list2[1] = list2[1], list2[0]
print(list2)


# ways to find length of a list
list3 = [1, 2, 3, 4, 5, 6]
print(len(list3))


# maximum number in a list
list4 = [ 20, 4, 89, 56, 3]
print(max(list4))

# minimum number in a list
list5 = [ 20, 4, 89, 56, 3]
print(min(list5))
